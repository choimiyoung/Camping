package vo;

public class MsgVo {

	int msg_idx;
	String msg_content;
	String msg_sender;
	String msg_receiver;
	String msg_sendtime;
	String msg_opentime;
	String msg_read_check;
	
	
	public int getMsg_idx() {
		return msg_idx;
	}
	public void setMsg_idx(int msg_idx) {
		this.msg_idx = msg_idx;
	}
	public String getMsg_content() {
		return msg_content;
	}
	public void setMsg_content(String msg_content) {
		this.msg_content = msg_content;
	}
	public String getMsg_sender() {
		return msg_sender;
	}
	public void setMsg_sender(String msg_sender) {
		this.msg_sender = msg_sender;
	}
	public String getMsg_receiver() {
		return msg_receiver;
	}
	public void setMsg_receiver(String msg_reciver) {
		this.msg_receiver = msg_reciver;
	}
	public String getMsg_sendtime() {
		return msg_sendtime;
	}
	public void setMsg_sendtime(String msg_sendtime) {
		this.msg_sendtime = msg_sendtime;
	}
	public String getMsg_opentime() {
		return msg_opentime;
	}
	public void setMsg_opentime(String msg_opentime) {
		this.msg_opentime = msg_opentime;
	}
	public String getMsg_read_check() {
		return msg_read_check;
	}
	public void setMsg_read_check(String msg_read_check) {
		this.msg_read_check = msg_read_check;
	}
	
	
}
